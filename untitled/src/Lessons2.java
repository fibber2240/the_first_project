
import java.util.Arrays;
public class Lessons2 {
        public static void main(String[] args) {
            int [][] numbers = new int[10][10];
            // объявляем массив размером 10*10
            for (int a =0; a<10; a++ ){
                for (int b=0; b<10;b++){
                    System.out.print(numbers [a][b] +" ");
                }
                System.out.println();
            }
        }
    }
